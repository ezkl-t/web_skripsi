Apakah kamu tahu bahwa tubuh kita dilengkapi berbagai sistem pertahanan yang melindungi diri dari serangan penyakit? Bagaimana tubuh kita bisa melindungi diri dari patogen penyebab penyakit? Apa saja yang ada pada sistem pertahanan tubuh kita?<br>
<br>

<img src=../img/MEKANISME-SISTEM-PERTAHANAN-TUBUH.png class="gambar-materi" style="width: 30%" alt="mekanisme sistem pertahanan tubuh">

<br>Fungsi sistem pertahanan tubuh sebagai berikut.<br>
a. Melindungi tubuh dari serangan benda asing atau bibit penyakit yang masuk ke tubuh. Benda asing tersebut dapat berupa mikrobia penyebab penyakit (patogen), misal virus, bakteri, Protozoa, dan jamur.<br>
b. Menghilangkan jaringan atau sel yang mati atau rusak (debris sel) untuk perbaikan jaringan.<br>
c. Mengenali dan menghilangkan sel abnormal. Itulah beberapa fungsi sistem pertahanan tubuh. Pertahanan tubuh terhadap suatu penyakit melibatkan antigen dan peran antibodi. Sebelum mempelajari lebih lanjut mengenai jenis-jenis pertahanan tubuh, lakukan terlebih dahulu kegiatan berikut untuk mendefinisikan antigen dan antibodi.<br>

